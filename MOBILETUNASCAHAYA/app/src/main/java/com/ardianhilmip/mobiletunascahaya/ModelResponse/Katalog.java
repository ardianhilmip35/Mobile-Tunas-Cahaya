package com.ardianhilmip.mobiletunascahaya.ModelResponse;

public class Katalog {
    private String id_katalog, gambar, nama_bangunan, alamat, deskripsi;

    public String getId_katalog() {
        return id_katalog;
    }

    public void setId_katalog(String id_katalog) {
        this.id_katalog = id_katalog;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getNama_bangunan() {
        return nama_bangunan;
    }

    public void setNama_bangunan(String nama_bangunan) {
        this.nama_bangunan = nama_bangunan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getDeskripsi() {
        return deskripsi;
    }

    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
}
